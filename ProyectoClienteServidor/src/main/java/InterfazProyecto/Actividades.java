    private void btnBorrarActividadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActividadesActionPerformed
        try {
            Connection nuevaConexion = DriverManager.getConnection("jdbc:mysql://localhost/paises?serverTimezone=UTC", "root","Rojo2001.");

            String comando_delete = "DELETE FROM t_actividades  WHERE id = ?";

            PreparedStatement comandoPreparado = nuevaConexion.prepareStatement(comando_delete);

            // comandoPreparado.setString(1,txtIDActualizar.getText());
            comandoPreparado.setInt(1, Integer.parseInt(txtIDActividades.getText()));

            comandoPreparado.executeUpdate();

            int fila = Integer.parseInt(txtIDActividades.getText());

            mt.removeRow(fila-1);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la Base de Datos "+ex.getMessage());
        }
    }//GEN-LAST:event_btnBorrarActividadesActionPerformed

    private void btnActualizarActividadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActividadesActionPerformed
        try {
            Connection nuevaConexion = DriverManager.getConnection("jdbc:mysql://localhost/paises?serverTimezone=UTC", "root","Rojo2001.");

            String comando_update = "UPDATE t_actividades SET  pais=?, actividad = ?,precio = ?, fecha = ? WHERE id = ?";

            PreparedStatement comandoPreparado = nuevaConexion.prepareStatement(comando_update);

            // comandoPreparado.setString(1,txtIDActualizar.getText());
            comandoPreparado.setString(1, txtActividadesPais.getText());
            comandoPreparado.setString(2, txtActividadActividades.getText());
            comandoPreparado.setDouble(3, Double.parseDouble(txtPrecioActividades.getText()));
            comandoPreparado.setString(4, txtFechaActividades.getText());

            comandoPreparado.setInt(5,Integer.parseInt(txtIDActividades.getText()));

            comandoPreparado.executeUpdate();

            int fila = Integer.parseInt(txtIDActividades.getText());

            mt.setValueAt(txtActividadesPais.getText(),fila-1,0 );
            mt.setValueAt(txtActividadActividades.getText(),fila-1,1 );
            mt.setValueAt(Double.parseDouble(txtPrecioActividades.getText()),fila-1,2 );
            mt.setValueAt(txtFechaActividades.getText(),fila-1,3 );

            txtActividadesPais.setText("");
            txtActividadActividades.setText("");
            txtPrecioActividades.setText("");
            txtFechaActividades.setText("");

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la Base de Datos "+ex.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActividadesActionPerformed

    private void btnAgregarActividadesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActividadesActionPerformed
        Actividad alojamiento = new Actividad();

        alojamiento.setPais(txtActividadesPais.getText());
        alojamiento.setActividad(txtActividadActividades.getText());
        alojamiento.setPrecio(Double.parseDouble(txtPrecioActividades.getText()));
        alojamiento.setFecha(txtFechaActividades.getText());

        mt.addRow(new Object[]{alojamiento.getPais(), alojamiento.getActividad(), alojamiento.getPrecio(), alojamiento.getFecha()});
        txtActividadesPais.setText("");
        txtActividadActividades.setText("");
        txtPrecioActividades.setText("");
        txtFechaActividades.setText("");

        //BASES DE DATOS
        try{
            Connection nuevaConexion = DriverManager.getConnection("jdbc:mysql://localhost/paises?serverTimezone=UTC", "root","Rojo2001.");
            String comando = "INSERT INTO t_actividades( pais, actividad, precio, fecha)"
            + "VALUES(?,?,?,?)";

            PreparedStatement comandoPreparado = nuevaConexion.prepareStatement(comando);

            comandoPreparado.setString(1, alojamiento.getPais());
            comandoPreparado.setString(2, alojamiento.getActividad());
            comandoPreparado.setDouble(3, alojamiento.getPrecio());
            comandoPreparado.setString(4, alojamiento.getFecha());

            comandoPreparado.executeUpdate();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la Base de Datos "+ex.getMessage());

        }
    }//GEN-LAST:event_btnAgregarActividadesActionPerformed

